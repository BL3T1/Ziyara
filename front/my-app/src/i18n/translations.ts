export const translations: Record<Locale, typeof en> = { en, ar }

function get(obj: Record<string, unknown>, path: string): string | undefined {
  const parts = path.split('.')
  let current: unknown = obj
  for (const p of parts) {
    if (current == null || typeof current !== 'object') return undefined
    current = (current as Record<string, unknown>)[p]
  }
  return typeof current === 'string' ? current : undefined
}

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  let value =
    get(translations[locale] as unknown as Record<string, unknown>, key) ??
    get(translations.en as unknown as Record<string, unknown>, key) ??
    key
  if (params && typeof value === 'string') {
    for (const [k, v] of Object.entries(params)) {
      value = value.split(`{${k}}`).join(String(v))
    }
  }
  return value
}
